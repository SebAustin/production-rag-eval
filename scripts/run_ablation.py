"""Retriever ablation on the FinanceBench test split.

Compares retriever configurations by how well each surfaces a question's gold
evidence chunk(s) — pure retrieval quality, no generation or LLM judge needed:

    bm25_only -> dense_only -> rrf -> rrf_rerank (full cascade)

Metrics per config: hit@10, recall@10, MRR over the 30 test questions. The gold
chunk ids for a question are exactly the chunks its evidence produced at index
time (deterministic ids), so a "hit" means the right passage was retrieved.

Writes docs/ablation_results.md.

Note on Contextual Retrieval: the live index embeds the *contextualized* text, so
all four configs already benefit from it. Isolating the +Contextual-Retrieval
contribution requires a parallel index built over raw ``chunk.text``; that second
index is out of scope for a single ablation run and is noted in the output.
"""

from __future__ import annotations

import asyncio
from pathlib import Path

from qdrant_client import QdrantClient

from rag_eval.config import Settings
from rag_eval.ingestion.loader import load_financebench, load_passages
from rag_eval.logging import configure_logging, get_logger
from rag_eval.retrieval import BM25Retriever, CohereReranker, DenseRetriever, rrf_fuse
from rag_eval.retrieval.metrics import hit_at_k, mean, recall_at_k, reciprocal_rank

log = get_logger(__name__)

_TEST_PATH = Path("data/calibration/test_split.jsonl")
_BM25_PATH = Path("data/index/bm25.pkl")
_OUTPUT = Path("docs/ablation_results.md")
_RRF_K = 60
_TOP_K = 10
_CONFIGS = ("bm25_only", "dense_only", "rrf", "rrf_rerank")


async def _ranked_ids_per_config(  # noqa: PLR0913 — wires the full retrieval cascade
    question: str,
    bm25: BM25Retriever,
    dense: DenseRetriever,
    reranker: CohereReranker,
    chunks_map: dict[str, object],
    settings: Settings,
) -> dict[str, list[str]]:
    """Return the ranked chunk-id list each config produces for one question."""
    bm25_results = bm25.retrieve(question, top_k=settings.bm25_top_k)
    dense_results = await dense.retrieve(question, top_k=settings.dense_top_k)
    fused = rrf_fuse([bm25_results, dense_results], k=_RRF_K)
    candidate_ids = [cid for cid, _ in fused[: settings.dense_top_k] if cid in chunks_map]
    reranked = await reranker.rerank(question, candidate_ids, chunks_map, top_n=_TOP_K)  # type: ignore[arg-type]
    return {
        "bm25_only": [cid for cid, _ in bm25_results],
        "dense_only": [cid for cid, _ in dense_results],
        "rrf": [cid for cid, _ in fused],
        "rrf_rerank": [cid for cid, _ in reranked],
    }


def _gold_ids(row: dict[str, object]) -> set[str]:
    """The chunk ids this question's evidence produced at index time."""
    return {chunk.chunk_id for chunk in load_passages([row])}


def _render_markdown(results: dict[str, dict[str, float]], n: int) -> str:
    lines = [
        "# Ablation results",
        "",
        f"Retriever ablation on the FinanceBench test split (n={n}). Metric is gold-",
        "evidence retrieval quality (no generation). Generated by `make ablation`.",
        "",
        "| Config | hit@10 | recall@10 | MRR |",
        "|---|---|---|---|",
    ]
    labels = {
        "bm25_only": "BM25 only",
        "dense_only": "Dense only (voyage-3-large)",
        "rrf": "BM25 + Dense + RRF",
        "rrf_rerank": "+ Cohere Rerank 3.5 (full cascade)",
    }
    for key in _CONFIGS:
        m = results[key]
        lines.append(
            f"| {labels[key]} | {m['hit_at_10']:.2f} | {m['recall_at_10']:.2f} "
            f"| {m['mrr']:.3f} |",
        )
    lines += [
        "",
        "> All configs run on the contextualized index, so Contextual Retrieval is "
        "already baked in. A true +Contextual-Retrieval ablation needs a parallel "
        "index over raw chunk text (see `scripts/run_ablation.py` docstring).",
        "",
    ]
    return "\n".join(lines)


async def _run() -> None:
    configure_logging()
    settings = Settings()  # values from env/.env

    rows = load_financebench(_TEST_PATH)
    bm25 = BM25Retriever.load(_BM25_PATH)
    chunks_map = {c.chunk_id: c for c in bm25.chunks}
    dense = DenseRetriever(
        QdrantClient(url=settings.qdrant_url),
        settings.qdrant_collection,
        settings.voyage_api_key,
        settings.voyage_model,
        settings.embedding_dim,
    )
    reranker = CohereReranker(settings.cohere_api_key)

    per_config: dict[str, dict[str, list[float]]] = {
        cfg: {"hit": [], "recall": [], "rr": []} for cfg in _CONFIGS
    }
    scored = 0
    for i, row in enumerate(rows):
        gold = _gold_ids(row)
        if not gold:
            continue
        ranked = await _ranked_ids_per_config(
            str(row["question"]), bm25, dense, reranker, chunks_map, settings
        )
        for cfg in _CONFIGS:
            ids = ranked[cfg]
            per_config[cfg]["hit"].append(float(hit_at_k(ids, gold, _TOP_K)))
            per_config[cfg]["recall"].append(recall_at_k(ids, gold, _TOP_K))
            per_config[cfg]["rr"].append(reciprocal_rank(ids, gold))
        scored += 1
        log.info("ablated_question", i=i + 1, of=len(rows))

    results = {
        cfg: {
            "hit_at_10": mean(per_config[cfg]["hit"]),
            "recall_at_10": mean(per_config[cfg]["recall"]),
            "mrr": mean(per_config[cfg]["rr"]),
        }
        for cfg in _CONFIGS
    }
    _OUTPUT.parent.mkdir(parents=True, exist_ok=True)
    _OUTPUT.write_text(_render_markdown(results, scored), encoding="utf-8")
    print(_render_markdown(results, scored))


def main() -> None:
    """Entrypoint for ``make ablation``."""
    asyncio.run(_run())


if __name__ == "__main__":
    main()
