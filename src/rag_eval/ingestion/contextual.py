"""Anthropic Contextual Retrieval (anthropic.com/news/contextual-retrieval, Nov 2024).

Before embedding, every chunk gets a 1-2 sentence context prefix generated by
Claude Haiku that situates it within its full document. The prefix is prepended
to the chunk body to form ``contextualized_text``, which is what we embed and
BM25-index. The original ``text`` is preserved untouched for citations.

Prefixes are cached in SQLite keyed by ``sha256(chunk_id + text[:100])`` so that
rebuilding the index does not re-pay the Haiku cost for unchanged chunks.
"""

from __future__ import annotations

import asyncio
import hashlib
import sqlite3
from pathlib import Path
from typing import TYPE_CHECKING

from rag_eval.logging import get_logger

if TYPE_CHECKING:
    from anthropic import AsyncAnthropic

    from rag_eval.generation.citations import Chunk

log = get_logger(__name__)

_DEFAULT_HAIKU = "claude-haiku-4-5-20251001"
_MAX_DOC_CHARS = 8000
_SYSTEM_PROMPT = "You are a document context summarizer. Respond in 1-2 sentences only."
_DEFAULT_CACHE_PATH = Path(".contextual_cache.sqlite")


def _cache_key(chunk: Chunk) -> str:
    """Stable cache key: chunk id plus a prefix of its body."""
    return hashlib.sha256(f"{chunk.chunk_id}{chunk.text[:100]}".encode()).hexdigest()


class _PrefixCache:
    """Tiny SQLite-backed string cache for contextual prefixes."""

    def __init__(self, path: Path) -> None:
        self._conn = sqlite3.connect(path)
        self._conn.execute(
            "CREATE TABLE IF NOT EXISTS prefixes (key TEXT PRIMARY KEY, prefix TEXT)",
        )
        self._conn.commit()

    def get(self, key: str) -> str | None:
        row = self._conn.execute(
            "SELECT prefix FROM prefixes WHERE key = ?",
            (key,),
        ).fetchone()
        return None if row is None else str(row[0])

    def put(self, key: str, prefix: str) -> None:
        self._conn.execute(
            "INSERT OR REPLACE INTO prefixes (key, prefix) VALUES (?, ?)",
            (key, prefix),
        )
        self._conn.commit()

    def close(self) -> None:
        self._conn.close()


class ContextualRetriever:
    """Generate and attach Contextual Retrieval prefixes to chunks."""

    def __init__(
        self,
        client: AsyncAnthropic,
        model: str = _DEFAULT_HAIKU,
        cache_path: Path = _DEFAULT_CACHE_PATH,
    ) -> None:
        self._client = client
        self._model = model
        self._cache = _PrefixCache(cache_path)

    async def add_context(self, chunk: Chunk, full_document_text: str) -> Chunk:
        """Return a copy of ``chunk`` with ``contextualized_text`` populated.

        Uses the cached prefix when available; otherwise calls Claude Haiku and
        caches the result.
        """
        key = _cache_key(chunk)
        prefix = self._cache.get(key)
        if prefix is None:
            prefix = await self._generate_prefix(chunk, full_document_text)
            self._cache.put(key, prefix)
        contextualized = f"{prefix}\n\n{chunk.text}" if prefix else chunk.text
        return chunk.model_copy(update={"contextualized_text": contextualized})

    async def _generate_prefix(self, chunk: Chunk, full_document_text: str) -> str:
        """Call Claude Haiku to produce a 1-2 sentence situating prefix."""
        user = (
            f"<document>{full_document_text[:_MAX_DOC_CHARS]}</document>\n"
            f"<chunk>{chunk.text}</chunk>\n"
            "Briefly describe what this chunk discusses in the context of the "
            "full document. Be specific: include company name, fiscal year, and "
            "metric if present."
        )
        response = await self._client.messages.create(
            model=self._model,
            max_tokens=128,
            temperature=0,
            system=_SYSTEM_PROMPT,
            messages=[{"role": "user", "content": user}],
        )
        return self._extract_text(response).strip()

    @staticmethod
    def _extract_text(response: object) -> str:
        """Concatenate the text blocks of a messages response."""
        content = getattr(response, "content", None)
        if not content:
            return ""
        parts: list[str] = []
        for block in content:
            text = getattr(block, "text", None)
            if isinstance(text, str):
                parts.append(text)
        return " ".join(parts)

    async def process_batch(
        self,
        chunks: list[Chunk],
        docs: dict[str, str],
        semaphore_n: int = 5,
    ) -> list[Chunk]:
        """Contextualize many chunks with bounded concurrency.

        ``docs`` maps ``source_filename`` -> full document text. A chunk whose
        document is missing falls back to using its own body as context.
        """
        semaphore = asyncio.Semaphore(semaphore_n)

        async def _one(chunk: Chunk) -> Chunk:
            async with semaphore:
                document = docs.get(chunk.source_filename, chunk.text)
                return await self.add_context(chunk, document)

        results = await asyncio.gather(*(_one(c) for c in chunks))
        log.info("contextualized_batch", n=len(results))
        return list(results)

    def close(self) -> None:
        """Close the underlying cache connection."""
        self._cache.close()
